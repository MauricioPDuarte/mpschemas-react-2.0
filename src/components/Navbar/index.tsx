interface NavItemProps extends FlexProps {
  icon: IconType;
  children: ReactText;
  link: string;
}


const NavItem = ({ icon, children, link, ...rest }: NavItemProps) => {
  return (
    <Link href={link} style={{ textDecoration: 'none' }} _focus={{ boxShadow: 'none' }}>
      <Flex
        align="center"
        p="4"
        mx="4"
        borderRadius="lg"
        role="group"
        cursor="pointer"
        _hover={{
          bg: 'cyan.400',
          color: 'white',
        }}
        {...rest}>
        {icon && (
          <Icon
            color='gray.600'
            mr="4"
            fontSize="16"
            _groupHover={{
              color: 'white',
            }}
            as={icon}
          />
        )}
        <Text color='gray.600' _groupHover={{
              color: 'white',
            }}>{children}</Text>
      </Flex>
    </Link>
  );
};

